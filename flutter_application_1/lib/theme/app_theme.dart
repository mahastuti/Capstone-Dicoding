  /// Data text style using JetBrains Mono for numerical data and statistics
  static TextStyle dataTextStyle({
    required bool isLight,
    double fontSize = 14,
    FontWeight fontWeight = FontWeight.w400,
  }) {
    final Color textColor = isLight ? textPrimaryLight : textPrimaryDark;
    return GoogleFonts.jetBrainsMono(
      fontSize: fontSize,
      fontWeight: fontWeight,
      color: textColor,
      letterSpacing: 0,
    );
  }

  /// Progress gradient for progress indicators
  static LinearGradient progressGradient({required bool isLight}) {
    return LinearGradient(
      colors: isLight
          ? [progressStartLight, progressEndLight]
          : [progressStartDark, progressEndDark],
      begin: Alignment.centerLeft,
      end: Alignment.centerRight,
    );
  }

  /// Box shadow for cards and elevated elements
  static List<BoxShadow> cardShadow({required bool isLight}) {
    return [
      BoxShadow(
        color: isLight ? shadowLight : shadowDark,
        blurRadius: 4,
        offset: const Offset(0, 2),
      ),
    ];
  }

  /// Box shadow for floating elements
  static List<BoxShadow> floatingShadow({required bool isLight}) {
    return [
      BoxShadow(
        color: isLight ? shadowLight : shadowDark,
        blurRadius: 8,
        offset: const Offset(0, 4),
      ),
    ];
  }
